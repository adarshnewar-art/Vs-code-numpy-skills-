# Write a program to find the a bsolute values of all the elements of the array using numpy:
import numpy as np

# Create an array of values
values = np.array([-1, 2, -3, 4, -5])

# Find the absolute values of all the elements
absolute_values = np.abs(values)

# Print the results
print("Original values:", values)
print("Absolute values:", absolute_values)