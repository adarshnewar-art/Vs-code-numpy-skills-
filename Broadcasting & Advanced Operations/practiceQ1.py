import numpy as np
arr = np.array([1, 2, 3, 4, 5])

# a) muliply arr by 100
# b) subract 10 from arr
# c) divide arr by 3 (floor division)
# d) square arr 

multiplied = arr * 100
subtracted = arr - 10
divided = arr // 3
squared = arr ** 2

print("Multiplied by 100:", multiplied)
print("Subtracted by 10:", subtracted)
print("Divided by 3:", divided)
print("Squared:", squared)